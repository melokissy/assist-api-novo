package model;

import java.util.Date;

/**
 *
 * @author Kissy de Melo
 */
public class Attachment {
    private int id;
    private byte[] file;
    private Ticket ticket;
    private User user; 
    private Date createdAt; 
    
    public Attachment(){}

    public Attachment(int id, byte[] file, Ticket ticket, User user, Date createdAt) {
        this.id = id;
        this.file = file;
        this.ticket = ticket;
        this.user = user;
        this.createdAt = createdAt;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public byte[] getFile() {
        return file;
    }

    public void setFile(byte[] file) {
        this.file = file;
    }

    public Ticket getTicket() {
        return ticket;
    }

    public void setTicket(Ticket ticket) {
        this.ticket = ticket;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public Date getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(Date createdAt) {
        this.createdAt = createdAt;
    }
    
    
}
